    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $user = User::session($request->email)->first();
        if($user == null){
            return redirect()->back()->with(['error' => 'Email Tidak Ada']);
        }
        $new_password = str_random(6);
        $user->password = bcrypt($new_password);
        $user->save();
        session(['email' => $request->email, 'password' => $new_password]);
        $nama = $user->username;
        Mail::to($user)->send(new SendEmail($new_password,$nama));
    }

